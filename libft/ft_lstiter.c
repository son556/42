#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst && f)
	{
		while (1)
		{
			if (!lst)
				break ;
			f(lst -> content);
			lst = lst -> next;
		}
	}
}
